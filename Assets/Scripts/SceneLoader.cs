namespace UnityEditorTools
{
    using UnityEngine;
    using UnityEngine.SceneManagement;

    public class SceneLoader : MonoBehaviour
    {
        /*************************************************************************************************
        *** Variables
        *************************************************************************************************/
        [SerializeField] private string sceneName = null;

        /*************************************************************************************************
        *** Start
        *************************************************************************************************/
        private void Start()
        {
        }

        /*************************************************************************************************
        *** Update
        *************************************************************************************************/
        private void Update()
        {
        }

        /*************************************************************************************************
        *** Properties
        *************************************************************************************************/

        /*************************************************************************************************
        *** Methods
        *************************************************************************************************/
        public void LoadScene()
        {
            SceneManager.LoadScene(sceneName);
        }
    }
}
